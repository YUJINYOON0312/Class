
public class RelationalTest {
	
	public static void main(String[] args) {
		int a=10, b=20;
		boolean result=true;//false
		result = a == b;
		System.out.println(result);//false
		System.out.println(a != b);//true
		System.out.println(a > b);//false
		System.out.println(a < b);//true
		System.out.println(a >= b);//false
		System.out.println(a <= b);//true
	}
	
}
