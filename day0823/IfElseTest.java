
public class IfElseTest {
	
	public static void main(String[] args) {
		System.out.println("프로그램 시작~");
		int num=1;
		
		if(num%2 == 1) {
			System.out.println(num+"은 홀수!");
		}else {//조건문이 false일때 실행하는 문장
			System.out.println(num+"은 짝수!");
		}
		
		System.out.println("프로그램 종료!");
		
		
	}

}
