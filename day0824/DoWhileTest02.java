package day0824;

public class DoWhileTest02 {

	public static void main(String[] args) {
	
		
		int out = 0;
			do{
				int i = 0;
				do {
					System.out.print("* ");
					i++;
				}while(i<5);
	
				System.out.println();
				out++;
			}while(out<5);
	}

}
