package day0824;

public class LoopTest {

	public static void main(String[] args) {
	// 구구단 츨력
	
		
		for(int num = 2; num <= 9; num++) {
		for(int i = 1; i<=9; i++) {
		
			System.out.println(num +" * "+ i + " = " + i * num);
		
			}
		
			System.out.println("");
		}

	}

} // 구구단 2-9단 출력
